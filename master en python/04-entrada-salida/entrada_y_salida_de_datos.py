"para ingresar datos por teclado se usa la funcion input "
var = input("ingrese un dato: ")
print(var)

"para ingresar datos numericos se debe especificar el tipo de dato"
var=int(input("ingrese un dato: "))
print(var)

"aqui se pueden ingresar valores con decimales"
var=float(input("ingrese un dato: "))
print(var)

"tambien se pueden ingresar datos true o false de tipo bool"
var=bool(input("ingrese un dato: "))
print(var)